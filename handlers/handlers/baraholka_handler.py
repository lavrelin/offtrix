async def show_baraholka_preview(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Show preview of baraholka post"""
    if 'baraholka_post' not in context.user_data:
        await update.callback_query.edit_message_text("‚ùå –î–∞–Ω–Ω—ã–µ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã")
        return
    
    post_data = context.user_data['baraholka_post']
    
    # –§–æ—Ä–º–∏—Ä—É–µ–º –ø—Ä–µ–≤—å—é
    category = post_data.get('category', 'üí∞ –ü—Ä–æ–¥–∞–º')
    text = post_data.get('text', '(–±–µ–∑ –æ–ø–∏—Å–∞–Ω–∏—è)')
    media = post_data.get('media', [])
    
    preview_text = f"üõí **–ü—Ä–µ–¥–ø—Ä–æ—Å–º–æ—Ç—Ä**\n\n"
    preview_text += f"üìÇ –†–∞–∑–¥–µ–ª: {category}\n\n"
    preview_text += f"üìù –û–ø–∏—Å–∞–Ω–∏–µ:\n{text}\n\n"
    preview_text += f"üì∏ –§–æ—Ç–æ: {len(media)} —à—Ç.\n\n"
    preview_text += f"#–ë–∞—Ä–∞—Ö–æ–ª–∫–∞ @hungarytrade"
    
    keyboard = [
        [
            InlineKeyboardButton("üì® –û—Ç–ø—Ä–∞–≤–∏—Ç—å –Ω–∞ –º–æ–¥–µ—Ä–∞—Ü–∏—é", callback_data=BAR_CALLBACKS['send']),
            InlineKeyboardButton("‚úèÔ∏è –ò–∑–º–µ–Ω–∏—Ç—å", callback_data=BAR_CALLBACKS['edit'])
        ],
        [InlineKeyboardButton("‚ùå –û—Ç–º–µ–Ω–∞", callback_data=BAR_CALLBACKS['cancel'])]
    ]
    
    try:
        await update.callback_query.delete_message()
    except:
        pass
    
    # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –º–µ–¥–∏–∞
    if media:
        for i, item in enumerate(media[:5]):
            try:
                caption = f"üì∏ –§–æ—Ç–æ {i+1}/{len(media)}" if i == 0 else None
                if item['type'] == 'photo':
                    await update.effective_message.reply_photo(item['file_id'], caption=caption)
                elif item['type'] == 'video':
                    await update.effective_message.reply_video(item['file_id'], caption=caption)
            except Exception as e:
                logger.error(f"Error showing media: {e}")
    
    # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ç–µ–∫—Å—Ç —Å –∫–Ω–æ–ø–∫–∞–º–∏
    await update.effective_message.reply_text(
        preview_text,
        reply_markup=InlineKeyboardMarkup(keyboard),
        parse_mode='Markdown'
    )

async def send_baraholka_to_moderation(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Send baraholka post to moderation"""
    user_id = update.effective_user.id
    post_data = context.user_data.get('baraholka_post')
    
    if not post_data:
        await update.callback_query.edit_message_text("‚ùå –î–∞–Ω–Ω—ã–µ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã")
        return
    
    try:
        if not db.session_maker:
            await update.callback_query.edit_message_text("‚ùå –ë–î –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–∞")
            return
        
        async with db.get_session() as session:
            result = await session.execute(select(User).where(User.id == user_id))
            user = result.scalar_one_or_none()
            
            if not user:
                await update.callback_query.edit_message_text("‚ùå –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω. /start")
                return
            
            # –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫—É–ª–¥–∞—É–Ω–∞
            can_post, remaining = await cooldown_service.check_cooldown(
                user_id, 'baraholka_post', Config.COOLDOWN_SECONDS
            )
            
            if not can_post and not Config.is_moderator(user_id):
                minutes = remaining // 60
                await update.callback_query.edit_message_text(
                    f"‚è≥ –°–ª–µ–¥—É—é—â–∏–π –ø–æ—Å—Ç —á–µ—Ä–µ–∑ {minutes} –º–∏–Ω—É—Ç"
                )
                return
            
            # –°–æ–∑–¥–∞—ë–º –ø–æ—Å—Ç
            post = Post(
                user_id=int(user_id),
                category='üõí –ë–∞—Ä–∞—Ö–æ–ª–∫–∞',
                subcategory=post_data.get('category'),
                text=str(post_data.get('text', ''))[:4096],
                media=list(post_data.get('media', [])),
                hashtags=['#–ë–∞—Ä–∞—Ö–æ–ª–∫–∞', post_data.get('category')],
                anonymous=False,
                status=PostStatus.PENDING,
                is_piar=False
            )
            session.add(post)
            await session.flush()
            await session.commit()
            await session.refresh(post)
            
            # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –≤ –≥—Ä—É–ø–ø—É –º–æ–¥–µ—Ä–∞—Ü–∏–∏
            await send_to_mod_group(update, context, post, user, post_data)
            
            # –û–±–Ω–æ–≤–ª—è–µ–º –∫—É–ª–¥–∞—É–Ω
            await cooldown_service.set_cooldown(user_id, 'baraholka_post', Config.COOLDOWN_SECONDS)
            
            # –û—á–∏—â–∞–µ–º –¥–∞–Ω–Ω—ã–µ
            context.user_data.pop('baraholka_post', None)
            context.user_data.pop('waiting_for', None)
            
            await update.callback_query.edit_message_text(
                "‚úÖ **–û–±—ä—è–≤–ª–µ–Ω–∏–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ –Ω–∞ –º–æ–¥–µ—Ä–∞—Ü–∏—é!**\n\n"
                "‚è∞ –û–∂–∏–¥–∞–π—Ç–µ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –æ –ø—É–±–ª–∏–∫–∞—Ü–∏–∏"
            )
            
    except Exception as e:
        logger.error(f"Error sending baraholka post: {e}", exc_info=True)
        await update.callback_query.edit_message_text("‚ùå –û—à–∏–±–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏")

async def send_to_mod_group(update: Update, context: ContextTypes.DEFAULT_TYPE,
                            post: Post, user: User, post_data: dict):
    """Send to moderation group"""
    bot = context.bot
    username = user.username or f"ID_{user.id}"
    category = post_data.get('category', 'üí∞ –ü—Ä–æ–¥–∞–º')
    
    mod_text = (
        f"üõí **–ù–û–í–û–ï –û–ë–™–Ø–í–õ–ï–ù–ò–ï - –ë–ê–†–ê–•–û–õ–ö–ê**\n\n"
        f"üë§ –û—Ç: @{username} (ID: {user.id})\n"
        f"üìÇ –†–∞–∑–¥–µ–ª: {category}\n\n"
        f"üìù –û–ø–∏—Å–∞–Ω–∏–µ:\n{post.text[:300]}"
    )
    
    if len(post.text) > 300:
        mod_text += "..."
    
    if post.media:
        mod_text += f"\n\nüì∏ –§–æ—Ç–æ: {len(post.media)} —à—Ç."
    
    keyboard = [[
        InlineKeyboardButton("‚úÖ –û–ø—É–±–ª–∏–∫–æ–≤–∞—Ç—å", callback_data=f"mod_app:{post.id}"),
        InlineKeyboardButton("‚ùå –û—Ç–∫–ª–æ–Ω–∏—Ç—å", callback_data=f"mod_rej:{post.id}")
    ]]
    
    try:
        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –º–µ–¥–∏–∞
        if post.media:
            for item in post.media[:3]:
                if item['type'] == 'photo':
                    await bot.send_photo(Config.MODERATION_GROUP_ID, item['file_id'])
                elif item['type'] == 'video':
                    await bot.send_video(Config.MODERATION_GROUP_ID, item['file_id'])
        
        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Ç–µ–∫—Å—Ç —Å –∫–Ω–æ–ø–∫–∞–º–∏
        await bot.send_message(
            Config.MODERATION_GROUP_ID,
            mod_text,
            reply_markup=InlineKeyboardMarkup(keyboard),
            parse_mode='Markdown'
        )
    except Exception as e:
        logger.error(f"Error sending to mod group: {e}")

